number1 = 10  # Correcting the variable name
number2 = 5

# Addition
sum_result = number1 + number2  # Renaming 'sum' to avoid conflicts with the built-in function
print("Sum of {0} and {1} is {2}".format(number1, number2, sum_result))

# Subtraction
difference = number1 - number2
print("Difference of {0} and {1} is {2}".format(number1, number2, difference))

# Multiplication
product = number1 * number2
print("Product of {0} and {1} is {2}".format(number1, number2, product))
